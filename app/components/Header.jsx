/**
 * @param {{
 *   isActive: boolean;
 *   isPending: boolean;
 * }}
 */
function activeLinkStyle({isActive, isPending}) {
  return {
    fontWeight: isActive ? '600' : undefined,
    color: isPending ? '#C4B5FD' : '#1F1F1F',
  };
}

